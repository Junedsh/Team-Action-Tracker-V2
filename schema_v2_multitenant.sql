-- 4. RLS POLICIES (The Magic Separation)

-- Helper function to get current user's department
-- This makes policies cleaner.
CREATE OR REPLACE FUNCTION get_my_department_id()
RETURNS UUID AS $$
  SELECT department_id FROM public.profiles WHERE id = auth.uid() LIMIT 1;
$$ LANGUAGE sql SECURITY DEFINER;

-- DEPARTMENTS:
-- Everyone can read departments (to check codes), but usually we filter this better.
-- For simplicity: Allow reading all (needed for "Join" check), but strictly control writes.
CREATE POLICY "Enable read access for authenticated users" ON public.departments FOR SELECT TO authenticated USING (true);
CREATE POLICY "Enable insert for authenticated users" ON public.departments FOR INSERT TO authenticated WITH CHECK (true); 

-- PROFILES:
-- Users can read profiles in their own department.
-- Users can insert/update their OWN profile.
CREATE POLICY "Read own department profiles" ON public.profiles FOR SELECT TO authenticated 
USING (department_id = get_my_department_id() OR id = auth.uid());

CREATE POLICY "Manage own profile" ON public.profiles FOR ALL TO authenticated 
USING (id = auth.uid()) WITH CHECK (id = auth.uid());

-- PROJECTS, TEAM_MEMBERS, TASKS:
-- Standard isolation: Only access if department_id matches user's department.

-- Projects
CREATE POLICY "Department Isolation" ON public.projects FOR ALL TO authenticated
USING (department_id = get_my_department_id())
WITH CHECK (department_id = get_my_department_id());

-- Team Members
CREATE POLICY "Department Isolation" ON public.team_members FOR ALL TO authenticated
USING (department_id = get_my_department_id())
WITH CHECK (department_id = get_my_department_id());

-- Tasks
CREATE POLICY "Department Isolation" ON public.tasks FOR ALL TO authenticated
USING (department_id = get_my_department_id())
WITH CHECK (department_id = get_my_department_id());

